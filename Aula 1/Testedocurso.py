# A função print() é usada para exibir informações na tela.
# Tudo o que estiver dentro dos parênteses será mostrado no terminal.

# Exemplo 1: Mostrando uma string (texto).
# Strings são definidas com aspas simples ou duplas.
print("ola mundo 1")

# Exemplo 2: Outra string.
print('ola mundo 2')

# Linhas que começam com # são comentários. O Python as ignora.
# Comentários são úteis para explicar o que o código faz.
# A linha abaixo, por exemplo, não será executada.
# print("Isso não vai aparecer")

# Comentários de múltiplas linhas (docstrings) podem ser feitos com três aspas.
# Embora sejam tecnicamente strings, são frequentemente usados para documentação.
'''
Isso é um comentário de bloco que o Python também ignora.
Ele pode ocupar várias linhas e é útil para explicações mais longas.
'''

# O código continua a ser executado normalmente após os comentários.
print("ola mundo 3")
print("ola mundo 4")
