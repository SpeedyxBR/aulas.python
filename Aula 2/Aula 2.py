# Em Python, existem diferentes operadores aritméticos para realizar cálculos.
# O operador // é usado para fazer uma "divisão inteira".

# A divisão inteira sempre retorna o resultado da divisão como um número inteiro,
# descartando qualquer parte decimal (o que vem depois da vírgula).

# Exemplo: 5 dividido por 2 é 2.5.
# Com a divisão inteira, o resultado será apenas 2.

# A função print() exibirá o resultado do cálculo.
print(5 // 2)

# Outros exemplos para entender melhor:
# print(10 // 3)  # O resultado seria 3 (pois 10/3 = 3.33...)
# print(7 // 2)   # O resultado seria 3 (pois 7/2 = 3.5)
# print(10 // 2)  # O resultado seria 5 (pois 10/2 = 5.0)
